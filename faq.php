<h2> Hoe kan ik iets bestellen? </h2>
<p> Ga naar het product dat je wilt bestellen, selecteer het aantal dat je wilt bestellen en klik dan op het winkelmandje. Als je klaar met bestellen, klik je op het winkelmandje waarbij je kunt afrekenen. </p>

